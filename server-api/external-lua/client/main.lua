--#############################################################################
--# Require the Coronium Core plugin
--#############################################################################
local core = require("plugin.coronium-core")

--#############################################################################
--# Initialize the plugin
--#############################################################################
core.init({
  version = 2, -- 1 for < v2.6.0 servers
  server = "http://<coronium.server.com>",
  key = "<coronium-server-api-key>",
  scope = "default",
  api = "MyApi"
})

--#############################################################################
--# Debug response
--#############################################################################
local function apiResponse(evt)
  core.debug(evt)
end

--#############################################################################
--# Call API
--#############################################################################
core.api.getOutput(apiResponse)